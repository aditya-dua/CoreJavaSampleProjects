package com.example.hashmaps;

public class Book {

	String authorName;
	String bookName;
	int price;
	int bookId;
	
	
	public Book(String authorName, String bookName, int price, int bookId) {
		super();
		this.authorName = authorName;
		this.bookName = bookName;
		this.price = price;
		this.bookId = bookId;
	}
	
	
	
	
}
